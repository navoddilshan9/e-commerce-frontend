class Seller {
    constructor(
      sellerId,
      sellerName,
      password,
      email,
      userName,
      address,
      location,
      contactNumber
    ) {
      this.sellerId = sellerId;
      this.sellerName = sellerName;
      this.password = password;
      this.email = email;
      this.userName = userName;
      this.address = address;
      this.location = location;
      this.contactNumber = contactNumber;
    }
  }
  
  export default Seller;
  